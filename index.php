        <h1>Serres*</h1>
        <img id="logo" src="img/serres.svg" alt="" srcset="">
        <p>Transdisciplinary mensual bilingual editon <br>Revue transdisciplinaire mensuelle bilingue</p>
        <p>Serres* comes together as a few pages to invest in where <em>multiple rhythms</em>, forms and angles collide. A project for which its frame is diaphanous, a space for experimentation, fragile tests, beginnings, intuitions. Serres is the desire to share these thoughts, to give them shape, give them space, to make them relate in the in between of stages of a process. <br> <br>
        L’opération de mise en forme nous intéresse ; celle des idées, des impressions, des gestes, qui se fait à chaque instant de la vie et qui trouve dans les œuvres une intensité renouvellée. Il s’agit d’en proposer des figures, à partir de divers points de vue par lesquels entrevoir la réalité : arts visuels, philosophie, sciences, poésie, savoirs-faire et plus encore. Dans les édifices théoriques, esthétiques, imaginaires, nous pensons trouver une fenêtre sur le monde, à travers laquelle il se trouve d'un même acte présenté et transformé. Alors s'érigent, ravivées, notre attention et notre attente.
        </p>
